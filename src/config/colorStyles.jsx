// Helper function to group colors by category
export const colorCategories = colorPresets.reduce((acc, color) => {
	if (!acc[color.category]) {
		acc[color.category] = [];
	}
	acc[color.category].push(color);
	return acc;
}, {});

// Export series-specific arrays if needed
export const lSeriesColors = colorPresets.filter(
	(color) => color.category === 'L Series'
);
export const mSeriesColors = colorPresets.filter(
	(color) => color.category === 'M Series'
);
export const zSeriesColors = colorPresets.filter(
	(color) => color.category === 'Z Series'
);

// Get color by ID helper function
export const getColorById = (id) =>
	colorPresets.find((color) => color.id === id);
